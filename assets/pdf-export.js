/**
 * BitsToBetter PDF Export Utility
 * Converts calculator results to professional PDF reports
 */

(function (global) {
  'use strict';

  const PDFExport = {
    /**
     * Generate a professional PDF report from calculator results
     * @param {Object} config - Configuration object
     * @param {string} config.title - Report title
     * @param {string} config.subtitle - Calculator name
     * @param {Array} config.results - Array of { label, value } results
     * @param {Array} config.metadata - Array of { key, value } inputs used
     * @param {string} config.chartHTML - Optional chart/visualization HTML
     * @param {string} config.disclaimer - Legal disclaimer text
     */
    generate: function(config) {
      const doc = new jsPDF('p', 'mm', 'a4');
      const pageWidth = doc.internal.pageSize.getWidth();
      const pageHeight = doc.internal.pageSize.getHeight();
      let yPos = 15;

      // Header
      doc.setFillColor(15, 23, 42); // Dark blue
      doc.rect(0, 0, pageWidth, 35, 'F');
      doc.setTextColor(255, 255, 255);
      doc.setFontSize(24);
      doc.text(config.title || 'Calculator Report', 15, 20);
      doc.setFontSize(12);
      doc.text(config.subtitle || 'BitsToBetter', 15, 28);
      doc.setTextColor(0, 0, 0);

      yPos = 45;

      // Main Results
      if (config.results && config.results.length > 0) {
        doc.setFontSize(14);
        doc.setFont(undefined, 'bold');
        doc.text('Results', 15, yPos);
        yPos += 10;

        doc.setFontSize(11);
        doc.setFont(undefined, 'normal');
        config.results.forEach(result => {
          doc.setFont(undefined, 'bold');
          doc.text(result.label + ':', 15, yPos);
          doc.setFont(undefined, 'normal');
          doc.text(result.value.toString(), 80, yPos);
          yPos += 8;

          // Check for page break
          if (yPos > pageHeight - 20) {
            doc.addPage();
            yPos = 15;
          }
        });
      }

      yPos += 5;

      // Input Metadata
      if (config.metadata && config.metadata.length > 0) {
        doc.setFontSize(12);
        doc.setFont(undefined, 'bold');
        doc.text('Input Parameters', 15, yPos);
        yPos += 8;

        doc.setFontSize(10);
        doc.setFont(undefined, 'normal');
        config.metadata.forEach(item => {
          doc.text(item.key + ': ' + item.value, 15, yPos);
          yPos += 6;

          if (yPos > pageHeight - 30) {
            doc.addPage();
            yPos = 15;
          }
        });
      }

      // Disclaimer
      if (config.disclaimer) {
        yPos += 10;
        doc.setFontSize(9);
        doc.setTextColor(100, 100, 100);
        doc.setFont(undefined, 'italic');
        doc.text(config.disclaimer, 15, yPos, { maxWidth: pageWidth - 30, align: 'left' });
      }

      // Footer
      const totalPages = doc.internal.pages.length - 1;
      doc.setFontSize(9);
      doc.setTextColor(150, 150, 150);
      for (let i = 1; i <= totalPages; i++) {
        doc.setPage(i);
        doc.text('Generated by BitsToBetter • https://bitstobetter.com', 15, pageHeight - 10);
        doc.text(`Page ${i} of ${totalPages}`, pageWidth - 30, pageHeight - 10);
      }

      return doc;
    },

    /**
     * Download PDF with filename
     */
    download: function(doc, filename) {
      doc.save(filename || 'calculator-report.pdf');
    },

    /**
     * Common calculator result formatters
     */
    formatters: {
      currency: (value, currency = '₹') => {
        return currency + ' ' + parseFloat(value).toLocaleString('en-IN', { maximumFractionDigits: 0 });
      },
      percentage: (value) => {
        return parseFloat(value).toFixed(2) + '%';
      },
      years: (value) => {
        return Math.round(value) + ' years';
      },
      number: (value) => {
        return parseFloat(value).toLocaleString('en-IN', { maximumFractionDigits: 2 });
      }
    }
  };

  global.BitsToBetterPDF = PDFExport;

})(window);

